
package first;


public class Q11 {
     static boolean isEven(int y)  {
         int x=0;
      for(int i=y; i>=0; i--){
        x=y-3;
       
      }
       if(x==0)
        return true;
         return false;
     }
     
     public static void main(String[] args) {
         System.out.println(isEven(10));
    }
}
